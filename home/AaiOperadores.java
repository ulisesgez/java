public class AaiOperadores {
    public static void main(String[] args) {
        /*
        Operador:
        Simbolo especial que indica al compilador que debe efectuar una determinada operacion.
        Estas operaciones pueden ser de asignacion, aritmeticas, condicionales, relacionales,
        entre otras.
         */
        double number1, number2, number3;
        number1 = 10;
        number2 = 20;
        number3 = 70;
        System.out.println("La suma de los tres numeros es:");
        System.out.println(number1 + number2 + number3);
    }
}